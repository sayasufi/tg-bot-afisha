async def _probe(client: httpx.AsyncClient, username: str) -> str:
    """OK (public+posts), EMPTY (reachable, preview off — keep, Telethon reads it), DEAD (404/gone — drop)."""
    try:
        r = await client.get(f"https://t.me/s/{username}")
    except httpx.HTTPError:
        return "ERR"
    if r.status_code in (404, 410):
        return "DEAD"
    if r.status_code != 200:
        return "EMPTY"
    return "OK" if "tgme_widget_message" in r.text else "EMPTY"


async def main(apply: bool) -> None:
    flat = [(cid, u, n, a) for cid, lst in CHANNELS_BY_CITY.items() for (u, n, a) in lst]
    sem = asyncio.Semaphore(8)
    results: dict[str, str] = {}
    async with httpx.AsyncClient(
        timeout=httpx.Timeout(15.0), follow_redirects=True,
        headers={"User-Agent": "Mozilla/5.0 (X11; Linux x86_64) AppleWebKit/537.36 Chrome/124.0 Safari/537.36"},
    ) as client:
        async def one(u: str) -> None:
            async with sem:
                results[u] = await _probe(client, u)
        await asyncio.gather(*(one(u) for _, u, _, _ in flat))

    by_status: dict[str, int] = {}
    for st in results.values():
        by_status[st] = by_status.get(st, 0) + 1
    print("probe:", by_status)
    dead = sorted(u for u, st in results.items() if st == "DEAD")
    if dead:
        print("DEAD (dropped):", ", ".join(dead))

    keep = [(cid, u, n, a) for (cid, u, n, a) in flat if results.get(u) != "DEAD"]
    added = skipped = 0
    async with WorkerAsyncSessionLocal() as db:
        for cid, u, n, a in keep:
            res = await db.execute(text(
                "insert into ref.telegram_channels (username, city_id, is_active, venue_name, venue_address) "
                "values (:u, :c, true, :n, :a) on conflict (username) do nothing"
            ), {"u": u, "c": cid, "n": n, "a": a})
            added += 1 if res.rowcount else 0
            skipped += 0 if res.rowcount else 1
        if apply:
            await db.commit()
        else:
            await db.rollback()
        total = await db.scalar(text("select count(*) from ref.telegram_channels where is_active"))
        rows = (await db.execute(text(
            "select city_id, count(*) from ref.telegram_channels where is_active group by city_id order by city_id"
        ))).all()
        per_city = {r[0]: r[1] for r in rows}
    print(f"{'APPLIED' if apply else 'DRY-RUN'}: candidates={len(flat)} kept={len(keep)} added={added} skipped_existing={skipped} active_total={total}")
    print("per-city active:", per_city)


if __name__ == "__main__":
    asyncio.run(main(apply="--dry-run" not in sys.argv))

